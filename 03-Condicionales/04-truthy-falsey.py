# Truthy (Verdaderos)
print(bool(True))
print(bool(1))
print(bool(123))
print(bool(1.1))
print(bool(-1.1))
print(bool(1j))
print(bool("Hola"))
print(bool([1,2,3]))    # lista/arreglo
print(bool({1,2,3}))    # set
print(bool((1,2,3)))    # tupla
#Falsey (Falsos)
print(bool(False))
print(bool(0))
print(bool(0.0))
print(bool(0j))
print(bool(""))
print(bool([]))    # lista/arreglo
print(bool({}))    # set
print(bool(()))    # tupla
print(bool(None))    # tupla