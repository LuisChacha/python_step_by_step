# suma
print(2+2)  #4

# resta
print(4-2)  #2

# multiplicacion
print(4*2)  #8

# division
print(2/4)  #0.5

# otras operaciones

# potencia
print(2 ** 3)   #8

# division sin decimal
print(5//4) #1

# modulo(lo que sobra de la division: residuo)
print (6%4) #2

# redondear : round()
print(round(3.1))  
print(round(3.9))  

# valor absoluto : abs()
print(abs(20))  #20
print(abs(-20))  #20
print(abs(-20.34))  #20